# luminance threshold
LUMA_THRESHOLD = 32

# row identifiers
R = "YX0123456789"


def calc_luma(color):
    r, g, b = color
    return (r * 299 / 1000) + \
        (g * 587 / 1000) +    \
        (b * 114 / 1000)


def image_to_hollerith(data, debug=False):
    """Convert (r, g, b) data from selected card image locations into Boolean
values representing the presence of a hole in a given row of the card"""

    result = []
    for index, row in enumerate(data):
        h = []
        if debug:
            print(index, end=" ")
        for color in row:
            luma = calc_luma(color)
            bw = (luma > LUMA_THRESHOLD)
            if debug:
                print(luma, bw)
            h.append(bw)
        result.append(h)
        if debug:
            print()
    return result


def hollerith_to_strings(out, debug=False):
    strings = []
    for t in out:
        s = ""
        for index, row in enumerate(t):
            if not row:
                s += R[index]
        if debug:
            print(s)
        strings.append(s)
    return strings

